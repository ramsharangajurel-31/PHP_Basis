<!-- 1. Display formatted output using echo and print -->
<?php
// Using echo
echo "Hello, World!<br>";
echo "This is PHP using echo.<br>";
echo "You can output ", "multiple strings ", "separated by commas.<br>";

// Using print
print "Hello again!<br>";
print "This is PHP using print.<br>";

// Using print inside a formatted string
$name = "Ramsharan";
$age = 25;
print "My name is $name and I am $age years old.<br>";

// Using echo inside a formatted string
echo "Welcome $name! You are $age years old.<br>";
?>
